							<h4>challenge</h4>
						</div>
						<div class="border thirteen columns">
							<h3>more work, less workers</h3>
							<p>During the recession, the graphics department downsized workers and increased orders, sameas many other companies around 2008. We went from 20 orders a day to 50. From 5 graphic designers to 2. Those of us remaining were instructed to innovate for efficiency.</p>

							<h3>unhappy customers</h3>
							<p>Before I worked there, proofs were faxed to customers. Some time later, Brady upgraded to PDF proofs via email. The document displayed a swatch of print, specifications the print shop needed and a rough idea of a final print.</p>
							<img src="images/proof-version-1.jpg" alt="first iteration of the proof" />
							<p class="caption">Early verion of the proof page.</p>

							<p>The roughness occasionally caused unhappy customers. In the image below, you can't tell if the text will be facing inwards or out, and you can't tell how far up the lanyard the imprint will be (without measuring). This left some surprised end users when they received their final product. Printers were blamed and customer service took the job of apologizing.</p>

							<h3>new printing facility</h3>
							<p>The company bought a production facility in China, in addition to the one they had stateside, in 2007. That print shop wasn't two cubes down from graphics anymore, so more specifications were needed on the proofs. With the language barrier and time difference, we needed to be super clear to minimize the risk of mistake. That meant, the proofs now had 3 parties to please; local production, overseas production and customers.</p>
						</div>
						<div class="three columns">
							<h4>process</h4>
						</div>
						<div class="border thirteen columns">
							<h3>apply web knowledge to work</h3>
							<p>During this time, I was freelancing on the side and inhaling everything on usability I could. It made complete sense to use the principals in '<a href="http://www.sensible.com/dmmt.html" target="blank">Don't make me think</a>' at work. The design had two main user types. For the customers, I designed for an easy scan. We knew they would not check every detail. They, generally, wanted the colors and print to look right in the 3D sample image. For production, I used the "conventions are your friend" axiom. They was familiar to the way the job-ticket looked, so I designed the proof to match that layout. It would be a simpler side-by-side check.</p>

							<h3>talk with customers</h3>
							<p>To understand how the customer was using these proofs, we chose three high ordering ones to discuss their thoughts of trial proofs. I promoted candidness and 'clear reasons why' in their responses. Having received plenty of design feedback from web projects, I understood how important it is to ask for specific feedback. Bad feedback is subjective. Good feedback gets to the core of why they want this font less bold.</p>

							<h3>testing</h3>
							<p>The new factory was acquired and the printers needed a proof they would understand, fast. Orders were coming in. I designed different versions and tested them with real jobs. China was able to test our proofs thoroughly, as they received roughly 20 proofs per day. One version had all of the information, but it was too long (two pages total). Another version had different layouts for domestic and overseas production, but that became confusing because some customers received two different looking proofs.</p>
							<img src="images/proof-version-2.jpg" alt="second iteration of the proof" />
							<p class="caption">This version fit the data China needed and the realistic imagery the customers wanted.</p>

							<p>Ultimately, I was part of a small team open to change. We didn't have to wait for approval to impliment. The platform we were testing on was perfect for incremental changes. And the high numbers we were working with meant we would hear feedback fast. A perfect environment for tiny tests. As we promoted rock solid communication, the end users and producers let us know how things worked and what could be better.</p>
